import java.util.Scanner;

public class ADominoPiling { // Problem link : https://codeforces.com/problemset/problem/50/A
    public static void ADominoPiling(){
        Scanner sc = new Scanner(System.in);

        int m = sc.nextInt();
        int n = sc.nextInt();

        System.out.println(m*n / 2);
    }
}
